#[derive(Debug)]
pub struct Tile {
    pub position: Vector2, 
    pub size: Vector2,
}

impl Tile {
    pub fn new(pos_x: usize, pos_y:usize) -> Self {
        let size : f32 = 40.0;
        Tile {
            position: Vector2 { 
                x: (pos_x as f32) * size,
                y: (pos_y as f32) * size
            },
            size:  Vector2 {
                x: size,
                y: size
            }
        }
    }
}

